package lista01.ex05;

public class Node {
    public long item;
    public Node left;
    public Node right;

    public long getItem() {
        return item;
    }
    public void setItem(long item) {
        this.item = item;
    }
    public Node getLeft() {
        return left;
    }
    public void setLeft(Node left) {
        this.left = left;
    }
    public Node getRight() {
        return right;
    }
    public void setRight(Node right) {
        this.right = right;
    }

    
}
